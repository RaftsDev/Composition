package com.company;

public class Ceiling {
    private int height;
    private int paintedCollor;

    public Ceiling(int height, int paintedCollor) {
        this.height = height;
        this.paintedCollor = paintedCollor;
    }

    public int getHeight() {
        return height;
    }

    public int getPaintedCollor() {
        return paintedCollor;
    }
}
